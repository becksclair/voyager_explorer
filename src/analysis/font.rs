//! Minimal embedded 5x7 bitmap font for axis labels on rendered diagnostics
//! images. Covers only the glyphs the renderers need; unknown characters are
//! drawn as blanks. Avoids pulling in a font-rasterization dependency.

use image::{Rgb, RgbImage};

pub const GLYPH_W: u32 = 5;
/// Horizontal advance per character, including 1px spacing.
pub const ADVANCE: u32 = GLYPH_W + 1;

/// Each glyph is 7 rows of 5 bits, most significant bit = leftmost pixel.
fn glyph(c: char) -> [u8; 7] {
    match c {
        '0' => [0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E],
        '1' => [0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E],
        '2' => [0x0E, 0x11, 0x01, 0x02, 0x04, 0x08, 0x1F],
        '3' => [0x1F, 0x02, 0x04, 0x02, 0x01, 0x11, 0x0E],
        '4' => [0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02],
        '5' => [0x1F, 0x10, 0x1E, 0x01, 0x01, 0x11, 0x0E],
        '6' => [0x06, 0x08, 0x10, 0x1E, 0x11, 0x11, 0x0E],
        '7' => [0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08],
        '8' => [0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E],
        '9' => [0x0E, 0x11, 0x11, 0x0F, 0x01, 0x02, 0x0C],
        '.' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x0C, 0x0C],
        '-' => [0x00, 0x00, 0x00, 0x1F, 0x00, 0x00, 0x00],
        '+' => [0x00, 0x04, 0x04, 0x1F, 0x04, 0x04, 0x00],
        ':' => [0x00, 0x0C, 0x0C, 0x00, 0x0C, 0x0C, 0x00],
        'k' => [0x10, 0x10, 0x12, 0x14, 0x18, 0x14, 0x12],
        'H' => [0x11, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11],
        'z' => [0x00, 0x00, 0x1F, 0x02, 0x04, 0x08, 0x1F],
        's' => [0x00, 0x00, 0x0F, 0x10, 0x0E, 0x01, 0x1E],
        'm' => [0x00, 0x00, 0x1A, 0x15, 0x15, 0x15, 0x15],
        _ => [0; 7],
    }
}

/// Draw `text` with its top-left corner at (x, y), clipped to the image.
pub fn draw_text(img: &mut RgbImage, text: &str, x: i64, y: i64, color: Rgb<u8>) {
    let mut cx = x;
    for c in text.chars() {
        let rows = glyph(c);
        for (dy, row) in rows.iter().enumerate() {
            for dx in 0..GLYPH_W {
                if row & (1 << (GLYPH_W - 1 - dx)) != 0 {
                    let px = cx + dx as i64;
                    let py = y + dy as i64;
                    if px >= 0 && py >= 0 && (px as u32) < img.width() && (py as u32) < img.height() {
                        img.put_pixel(px as u32, py as u32, color);
                    }
                }
            }
        }
        cx += ADVANCE as i64;
    }
}

/// Pixel width of `text` when drawn with [`draw_text`].
pub fn text_width(text: &str) -> u32 {
    text.chars().count() as u32 * ADVANCE
}
